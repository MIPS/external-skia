static void test_perspective_nofilter(skiatest::Reporter* reporter) {
   int width = 32;
   int height = 32;

   SkBitmap src_b;
   src_b.setConfig(SkBitmap::kARGB_8888_Config, width, height);
   src_b.allocPixels();
   src_b.setPixels((void*)referent_png, NULL);

   SkPaint paint, paint_r;
   Mesh mesh(src_b, &paint, SkShader::kClamp_TileMode, SkShader::kClamp_TileMode);
   Mesh mesh_r(src_b, &paint_r, SkShader::kRepeat_TileMode, SkShader::kRepeat_TileMode);

   int sx, sy;
   sx = 2; sy = 2;
   SkBitmap dst_b, dst_b_r, dst_b_r_ref, dst_b_ref;
   dst_b.setConfig(SkBitmap::kARGB_8888_Config, width, height);
   dst_b.allocPixels();
   dst_b.eraseColor(0);
   dst_b_r.setConfig(SkBitmap::kARGB_8888_Config, width, height);
   dst_b_r.allocPixels();
   dst_b_r.eraseColor(0);
   dst_b_ref.setConfig(SkBitmap::kARGB_8888_Config, width, height);
   dst_b_ref.allocPixels();
   dst_b_ref.setPixels((void*)perspective_nofilter_clamp_referent_png, NULL);
   dst_b_r_ref.setConfig(SkBitmap::kARGB_8888_Config, width, height);
   dst_b_r_ref.allocPixels();
   dst_b_r_ref.setPixels((void*)perspective_nofilter_repeat_referent_png, NULL);

   SkCanvas canvas(dst_b);
   SkCanvas canvas_r(dst_b_r);

   SkMatrix mat;
   mat.reset();
   SkScalar sinV, cosV;
   SkScalar deg = 30;
   sinV = SkScalarSinCos(SkDegreesToRadians(deg), &cosV);
   SkPoint src[4], dst[4];

   src[0].fX = 0; src[0].fY = 0;
   src[1].fX = width; src[1].fY = 0;
   src[2].fX = width; src[2].fY = height;
   src[3].fX = 0; src[3].fY = height;

   dst[0].fX = 0; dst[0].fY = 0;
   dst[1].fX = width*cosV; dst[1].fY = (sinV/5)*height/2;     //sinV/5 thing is temporary, need to put a better condition
   dst[2].fX = width*cosV; dst[2].fY = height-sinV/5*height/2;
   dst[3].fX = 0; dst[3].fY = height;
   mat.setPolyToPoly(src, dst, 4);
   canvas.resetMatrix();
   canvas.concat(mat);
   mesh.draw(&canvas, &paint);

   SkMatrix mat_r;
   mat_r.reset();
   SkScalar sinV_r, cosV_r;
   SkScalar deg_r = 30;
   sinV_r = SkScalarSinCos(SkDegreesToRadians(deg_r), &cosV_r);
   SkPoint src_r[4], dst_r[4];

   src_r[0].fX = 0; src_r[0].fY = 0;
   src_r[1].fX = width; src_r[1].fY = 0;
   src_r[2].fX = width; src_r[2].fY = height;
   src_r[3].fX = 0; src_r[3].fY = height;

   dst_r[0].fX = 0; dst_r[0].fY = 0;
   dst_r[1].fX = width*cosV_r; dst_r[1].fY = (sinV_r/5)*height/2;     //sinV/5 thing is temporary, need to put a better condition
   dst_r[2].fX = width*cosV_r; dst_r[2].fY = height-sinV_r/5*height/2;
   dst_r[3].fX = 0; dst_r[3].fY = height;
   mat_r.setPolyToPoly(src_r, dst_r, 4);
   canvas_r.resetMatrix();
   canvas_r.concat(mat_r);
   mesh_r.draw(&canvas_r, &paint_r);

   if (memcmp(dst_b.getPixels(), dst_b_ref.getPixels(), dst_b.getSize())) {
      SkString str;
      str.printf("Perspective_nofilter CLAMP version FAILED!\n");
      reporter->reportFailed(str);
   }
   if (memcmp(dst_b_r.getPixels(), dst_b_r_ref.getPixels(), dst_b_r.getSize())) {
      SkString str;
      str.printf("Perspective_nofilter REPEAT version FAILED!\n");
      reporter->reportFailed(str);
   }
}

